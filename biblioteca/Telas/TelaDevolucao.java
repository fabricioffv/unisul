    private ArrayList<Emprestimo> verificaEmprestimosPublicacao(Publicacao p) throws SQLException{
        ArrayList<Emprestimo> lista = null;
        ArrayList<Emprestimo> listaRetorno = new ArrayList<Emprestimo>();
        EmprestimoDAO edao = new EmprestimoDAO();

        
        try {
            lista = edao.consultar(p);
            if (lista != null) {
                for (Emprestimo l : lista) {
                    if (!l.estaDevolvido()) {
                        listaRetorno.add(l);
                    }
                }
                if(listaRetorno.size() > 0){
                   return listaRetorno; 
                }else{
                    return null;
                }
            } else {
                return null;
            }
        } catch (SQLException ex) {
            return null;
        }
        
    }
    
    private void preencheFormulario(int codigo, PublicacaoDAO dao, JRadioButton botaoTipo){
        Publicacao p = null;
        
        try {
            p = dao.consultar(codigo); //resgata o objeto publicacao
        } catch (SQLException ex) {
            Logger.getLogger(TelaDevolucao.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(p != null){ //achou uma publicacao com o codigo
            ArrayList<Emprestimo> lista = null; 
            try {
                lista = verificaEmprestimosPublicacao(p);
                if(lista != null){
                    botaoTipo.setSelected(true);
                    jTextFieldCodigo.setText(Integer.toString(p.getCodigo()));
                    jTextFieldTitulo.setText(p.getTitulo());
                    jTextFieldAutor.setText(p.getAutor());
                    
                    DefaultTableModel df = (DefaultTableModel) jTableEmprestimos.getModel();
                    df.setRowCount(lista.size());
                    
                    for(int i= 0; i < lista.size(); i++){
                        jTableEmprestimos.setValueAt(lista.get(i).getCodigo(), i, 0);
                        jTableEmprestimos.setValueAt(lista.get(i).getUsuario().getNome(), i, 1);
                        jTableEmprestimos.setValueAt(lista.get(i).dataToString(lista.get(i).getDataEmprestimo()), i, 2);
                        jTableEmprestimos.setValueAt("Pendente", i, 3);
                        
                    }

                }else{
                    JOptionPane.showMessageDialog(null, p.getClass().getSimpleName() + " sem empréstimos pendentes");
                    limparCampos();
                }
            } catch (SQLException ex) {
                Logger.getLogger(TelaDevolucao.class.getName()).log(Level.SEVERE, null, ex);
            }   
        }else{
            JOptionPane.showMessageDialog(null, "Publicação não encontrada.");
            limparCampos();
        }
    }
    
    private void limparCampos(){
        this.grupoTipos.clearSelection();
        this.jTextFieldCodigo.setText("");
        this.jTextFieldTitulo.setText("");
        this.jTextFieldAutor.setText("");
        
        for(int i= 0; i < jTableEmprestimos.getRowCount(); i++){
            jTableEmprestimos.setValueAt("", i, 0);
            jTableEmprestimos.setValueAt("", i, 1);
            jTableEmprestimos.setValueAt("", i, 2);
            jTableEmprestimos.setValueAt("", i, 3);
            
        }
        jTableEmprestimos.clearSelection();
        DefaultTableModel df = (DefaultTableModel) jTableEmprestimos.getModel();
        df.setRowCount(0);
    }
    
    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        if(!TelaPrincipal.validaNumero(jTextFieldCodigo)){
            JOptionPane.showMessageDialog(null, "Campo " + jLabelCodigo.getText().replace(":", "") + 
                                          " não pode ser zero, conter letras ou estar em branco!" ,"Informação",JOptionPane.INFORMATION_MESSAGE);
            jTextFieldCodigo.grabFocus();
        }else{
            int codigo = Integer.parseInt(jTextFieldCodigo.getText());
            Publicacao p = null;
            PublicacaoDAO dao = null;
            
            if((!jRadioButtonLivro.isSelected()) && (!jRadioButtonPeriodico.isSelected())){
                JOptionPane.showMessageDialog(null, "Selecione um tipo!");
            }else{
                if(jRadioButtonLivro.isSelected()){ 
                    dao = new LivroDAO();
                    preencheFormulario(codigo, dao , jRadioButtonLivro);
                }else{
                    dao = new PeriodicoDAO();
                    preencheFormulario(codigo, dao , jRadioButtonPeriodico);
                }
            } 
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

