    private void jButtonDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDevolverActionPerformed
        if(jTableEmprestimos.getSelectedRow() >= 0){
            int linha = jTableEmprestimos.getSelectedRow();
            int codigo = (int) jTableEmprestimos.getValueAt(linha, 0);
            
            if(JOptionPane.showConfirmDialog(null, "Deseja devolver o empréstimo? ","Confirmação",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                EmprestimoDAO edao = new EmprestimoDAO();
                try {
                    if(edao.devolverEmprestimo(codigo)){
                        JOptionPane.showMessageDialog(null, "Empréstimo devolvido com sucesso!");
                        DefaultTableModel df = (DefaultTableModel) jTableEmprestimos.getModel();
                        df.removeRow(linha);
                        jTableEmprestimos.clearSelection();
                        limparCampos();
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(TelaDevolucao.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um registro!");
        }
        
        
    }//GEN-LAST:event_jButtonDevolverActionPerformed

